class Solution:
    def longestValidParentheses(self, s: str) -> int:
        if len(s) < 2 :
            return 0
        left = right = longest = 0
        for x in s :
            if x == '(':
                left += 1
            else :
                right += 1
            if right > left:
                left = right = 0
            elif right == left:
                if longest < right * 2:
                    longest = right * 2
        left = right = 0
        for x in s[::-1]:
            if x == '(':
                left += 1
            else :
                right += 1
            if left > right:
                left = right = 0
            elif right == left:
                if longest < left * 2:
                    longest = left * 2
        return longest

